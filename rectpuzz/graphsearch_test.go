package rectpuzz

import "testing"

func TestGraphSearch(t *testing.T) {
	res, err := SearchGraph(PuzzNumGrid)

	if err != nil {
		t.Error(err)
	}

	if ln := len(res); ln != 14 {
		t.Errorf("expected 14 Rects to result from the graph search of "+
			"PuzzNumGrid; received %v Rects", ln)
	}
}

